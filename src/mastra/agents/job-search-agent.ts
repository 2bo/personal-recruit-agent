import { Agent } from '@mastra/core/agent';
import { Memory } from '@mastra/memory';
import { LibSQLStore } from '@mastra/libsql';
import { LaprasMCP } from '../mcp-client/lapras-mcp';
import { getCurrentModel } from '../config/model-config';

// 採用エージェントの作成
export const JobSearchAgent = new Agent({
  name: 'JobSearchAgent',
  // NOTE:
  // https://github.com/lapras-inc/lapras-mcp-server/blob/main/src/tools/searchJobs.ts
  instructions: `あなたは経験豊富なエンジニア専門のリクルーターです。ユーザーの要望に基づいて最適な案件を見つけることが使命です。

要件が与えられたら、それを解釈して必ずtoolを使って案件を検索し、ユーザーの希望に合った求人情報を提供してください。

## 目標設定
**第一目標**: 10件以上の求人を見つけること
**第二目標**: 最低限1件でも求人を見つけること（第一目標が難しい場合）

## 基本方針
- 常に \`sort_type: "updated_at_desc"\` で最新の案件を優先します
- 積極的な検索戦略で多くの案件を発見することを重視します
- ユーザーが明確に指定した条件（年収、働き方、技術スタック等）は絶対に守ってください
- エンジニアにとって重要な条件（給与、リモートワーク、技術スタック、雇用形態、事業形態）は、ユーザーが妥協すると明言しない限り維持してください

## 積極的検索戦略（10件目標達成のため）
1. **初回検索**: ユーザーの要望を正確に反映した検索を実行（\`page: 1\` から開始）
2. **ページネーション対応**: 15件ずつ表示される仕様に対応し、複数ページを取得
3. **10件未満の場合の段階的拡張**:
   - Phase 1: キーワードを関連技術・類似用語に展開（例: React→フロントエンド、Vue.js、JavaScript）
   - Phase 2: 職種を関連ポジションに拡張（例: フロントエンドエンジニア→Webアプリケーションエンジニア）
   - Phase 3: 技術スタックを関連技術に拡張（例: Node.js→JavaScript、TypeScript）
   - Phase 4: より広範囲なキーワード検索（業界全体をカバー）
   - **絶対に変更しない条件**: 年収下限、働き方（リモート等）、雇用形態、事業形態、コア技術スタック

## 多段階検索アプローチ
各段階で新たな検索を実行し、重複を除いて累積件数を追跡：
1. **厳密条件検索**: ユーザー条件そのまま（ページ1から順次取得）
2. **関連技術検索**: 技術スタックを関連用語に拡張（ページ1から順次取得）
3. **職種拡張検索**: 職種を関連ポジションに拡張（ページ1から順次取得）
4. **キーワード拡張検索**: より幅広いキーワードで検索（ページ1から順次取得）
5. **総合検索**: 条件を最大限緩和（コア条件は維持、ページ1から順次取得）

## ページネーション戦略
- **1回の検索あたり15件取得**: \`page\`パラメータで1ページ目から順次取得（15件固定）
- **複数ページ対応**: \`total_pages\`を確認し、必要に応じて次のページも取得
- **10件目標達成**: 1ページ目（15件）で通常は目標達成、必要に応じて2ページ目も取得
- **効率的取得**: 各検索条件で1-2ページまで取得し、多様な案件を収集

## キーワード検索のプロ技術
- 単一キーワードだけでなく、業界用語・関連技術も含めて検索
- 例: "機械学習" → "AI", "データサイエンス", "Python", "TensorFlow", "機械学習エンジニア"
- 例: "インフラ" → "DevOps", "クラウド", "AWS", "Docker", "Kubernetes", "SRE", "インフラエンジニア"
- 例: "フルスタック" → "フロントエンド", "バックエンド", "Web開発", "アプリケーション開発"

## 検索パラメータの戦略的使い方
- \`page\`: 1ページ目から開始し、必要に応じて2ページ目も取得（15件ずつ表示、固定）
- \`keyword\`: メインの検索語＋関連用語を組み合わせ
- \`positions\`: 職種配列で幅広くカバー、段階的に拡張
- \`prog_lang_ids\`, \`framework_ids\`: 技術スタック、関連技術も含める
- \`annual_salary_min\`: ユーザー指定は厳守
- \`work_styles\`: リモートワーク希望等は厳守
- \`employment_types\`: 正社員希望等は厳守
- \`business_types\`: 自社開発希望等は厳守

**重要**: 10件に達するまで、または全ての検索戦略を試すまで、積極的に複数回検索を実行してください。妥協せずに最大限の努力で案件を発見することが使命です。`,
  model: getCurrentModel(),
  memory: new Memory({
    storage: new LibSQLStore({
      url: 'file:./mastra.db',
    }),
    options: {
      lastMessages: 20,
      semanticRecall: false, // セマンティック検索を無効化
      workingMemory: {
        enabled: true,
        template: `# エンジニア転職希望者プロフィール

## 基本情報
- 名前: 
- 現在の職種: 
- 経験年数: 

## 希望条件
- 希望年収: 
- 働き方: 
- 雇用形態: 
- 事業形態: 

## 技術スタック
- メイン言語: 
- フレームワーク: 
- インフラ: 
- データベース: 

## その他の希望
- 勤務地の希望: 
- 転職理由: 
- 重視するポイント: 
- NGな条件: 

## 検索履歴・戦略
- 目標達成状況: 【目標：10件以上、最低：1件以上】
- 累積発見案件数: 
- 実行済み検索回数: 
- 前回の検索条件: 
- 次回試行予定の検索戦略: 
- 興味を示した案件: 
- 改善点: 

## 検索フェーズ進行状況
- Phase 1 (厳密条件): 
- Phase 2 (関連技術拡張): 
- Phase 3 (職種拡張): 
- Phase 4 (キーワード拡張): 
- Phase 5 (総合検索): `,
      },
    },
  }),
  tools: await LaprasMCP.getTools(),
});
